        <h1>I. Academic Calendar</h1>
        <h3>Section 1</h3>
        <p>The university academic calendar shall be prepared in accordance with the issuance of the Commission on Higher Education (tertiary level) and the Department of Education (basic education level). The details shall be prepared by the Director for Student Admission and Records.</p>
      </article>
    <article>
        <h3>Section 2</h3>
        <p>Each academic calendar shall consist of not less than forty (40) weeks. Each semester for graduate and Undergraduate courses shall not be less than eighteen (18) weeks and a midyear term shall not be less than six (6) weeks.</p>
      </article>
    <article>
        <h3>Section 3</h3>
        <p>A course work during a midyear term shall be equivalent to a course work in one semester.</p>
      </article>
    <article>
        <h3>Section 4</h3>
        <p>All class hours lost due to fortitous events such as typhoons, earthquakes, epidemics, and outher natural calamities and/or due to University activities beyond the alotted number of school days in the university academic calendar shall be made up for.</p>
      </article>
    <article>
        <h3>Section 5</h3>
        <p>All academic, co-curricural, and other activities of the Univeristyin an academic year shall be incorporated in the University Academic calendar.</p>
      </article>
    <article>
        <h3>Section 6</h3>
        <p>During each academic year, the University shall be authorized a 2-3 days period to observe its Foundation day, except when the University schedules a shorter or a longer period.</p>
      </article>

      
