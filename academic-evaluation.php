    <h2>SECTION 1 RESIDENCE CREDITS</h2>
    <p><br>A. Residence credits refer to all academic units earned and validated by the University for a particular degree.
    </p>

    <p>B. Only residence credits are considered in evaluating subjects and units for a particular degree.
    </p>


    <h2>SECTION 2 STUDENT CLASSIFICATION</h2>
    <p><br>A. A full time/regular student is one who carries the full load in any given semester as specified in the curriculum.
    </p>

    <p>B. A part-time/irregular student is one who carries more or less than the full load specified in the curriculum.
    </p>

    <p>C. A transferee is one who comes from another institution where he started studying for a course and who is now registered in DMMMSU after qualifying for admission.
    </p>

    <p>D. A foreign student is one who is not citizen of the Philippines.
    </p>
   

